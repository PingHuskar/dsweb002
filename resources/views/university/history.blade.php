    <div class="container" style="text-indent: 40px; padding-bottom: 50px; line-height: 2; font-size: 22px;">
      <h1 style="text-align:center;">ประวัติหลักสูตร</h1>
        <p>มหาวิทยาลัยสวนดุสิตมุ่งเน้นผลิตบัณฑิตภายใต้ปรัชญาการเป็นมหาวิทยาลัยที่สามารถสร้างความเข้มแข็งในการอยู่รอดอย่างยั่งยืน บนพื้นฐานของการจัดการคุณภาพ จึงเป็นสถาบันการศึกษาทางวิชาการและวิชาชีพขั้นสูง โดยมีเป้าหมายชัดเจนที่จะสร้างบัณฑิต พัฒนางานวิจัย ให้บริการทางวิชาการแก่สังคมและท้องถิ่น ริเริ่ม ปรับปรุง ถ่ายทอด และพัฒนาองค์ความรู้ในด้านที่มีความเชี่ยวชาญและทำนุบำรุงศิลปะและวัฒนธรรม</p>
        <p>ดังนั้นจึงจำเป็นต้องพัฒนาหลักสูตรใหม่ที่มีความสอดคล้องกับปรัชญาและพันธกิจของมหาวิทยาลัย และสอดคล้องกับพันธกิจในแผนพัฒนาดิจิทัลเพื่อเศรษฐกิจและสังคมของประเทศไทย ปี พ.ศ. 2559 และประเด็นยุทธศาสตร์การพัฒนาประเทศในช่วงแผนพัฒนาเศรษฐกิจและสังคมแห่งชาติ ฉบับที่ 12 (พ.ศ. 2560-2564) ที่ให้ความสำคัญกับการใช้องค์ความรู้ทางวิทยาศาสตร์ ผลงานวิจัยและพัฒนาความก้าวหน้าทางเทคโนโลยี นวัตกรรมและความคิดสร้างสรรค์ ทั้งในภาคธุรกิจ ภาครัฐ และภาคประชาสังคม โดยมุ่งพัฒนาหลักสูตรเชิงรุกให้มีศักยภาพในการผลิตบุคลากรที่มีความสามารถในการพัฒนางานวิจัย นวัตกรรมทางด้านการวิเคราะห์ข้อมูลขนาดใหญ่  เพื่อให้มีความเหมาะสมกับองค์กร และมีความสามารถในการเรียนรู้เทคโนโลยีที่มีความเปลี่ยนแปลงอยู่ตลอดเวลา รวมทั้งสามารถรองรับการแข่งขันทางธุรกิจด้านเทคโนโลยีดิจิทัล ทั้งในประเทศและต่างประเทศได้เป็นอย่างดี</p>
        <p>หลักสูตรวิทยาการข้อมูลและการวิเคราะห์ เป็นหลักสูตรใหม่ ได้รับการพัฒนามาตั้งแต่ปี พ.ศ. 2560 โดยมีการแต่งตั้งคณะกรรมการพัฒนาหลักสูตร มีการประชุมและพัฒนาหลักสูตร จนได้หลักสูตรฉบับร่าง (มคอ.2, 3 และ 4) เสนอต่อที่ประชุมคณะกรรมการคณะวิทยาศาสตร์และเทคโนโลยี  พิจารณาหลักสูตรในการประชุม ครั้งที่ 1/2560 เมื่อวันที่ 13 มกราคม พ.ศ. 2560 และมีการปรับปรุงและแก้ไขหลักสูตรเพิ่มเติม จากนั้นได้นำเสนอหลักสูตรต่อคณะกรรมการกลั่นกรองหลักสูตรฯ พิจารณาหลักสูตรในการประชุม ครั้งที่ 1(1)/2560 เมื่อวันที่ 9 กุมภาพันธ์ พ.ศ. 2560 และ ครั้งที่ 2(2)/2560 เมื่อวันที่ 13 มีนาคม พ.ศ. 2560 คณะกรรมการพัฒนาหลักสูตรแก้ไขตามข้อเสนอแนะจากที่ประชุมและเข้านำเสนอหลักสูตรใหม่ต่อคณะกรรมการสภาวิชาการ พิจารณาเห็นชอบหลักสูตรในการประชุมครั้งที่ 5(11)/2560 เมื่อวันที่ 21 มีนาคม 2560 จากนั้นจึงนำเสนอหลักสูตรต่อที่ประชุมสภามหาวิทยาลัยสวนดุสิตพิจารณาอนุมัติและอนุมัติการแก้ไขหลักสูตร จำนวน 3 ครั้ง คือ ครั้งที่ 1 อนุมัติหลักสูตรในการประชุม ครั้งที่ 4(10)/2560 เมื่อวันที่ 31 มีนาคม พ.ศ. 2560 ครั้งที่ 2 อนุมัติการแก้ไขหลักสูตรในการประชุมครั้งที่ 7(13)/2560 เมื่อวันที่ 26 พฤษภาคม พ.ศ. 2560 และครั้งที่  3 อนุมัติการแก้ไขหลักสูตรในการประชุมครั้งที่ 13(19)/2560 เมื่อวันที่ 24 พฤศจิกายน พ.ศ. 2560  ที่ประชุมมีมติอนุมัติให้หลักสูตรสามารถเปิดการเรียนการสอนได้ และได้ส่งหลักสูตรให้สำนักงานคณะกรรมการการอุดมศึกษา (สกอ.) รับทราบการให้ความเห็นชอบหลักสูตรนี้แล้ว  และเริ่มดำเนินการรับนักศึกษาในปีการศึกษา 2561  เป็นต้นมา</p>
        <p>หลักสูตรวิทยาการข้อมูลและการวิเคราะห์ เป็นหลักสูตรใหม่ มีเป้าหมายที่สำคัญ คือ ต้องการผลิตบัณฑิตที่มีความรู้ ความสามารถ ความคิดวิเคราะห์อย่างเป็นระบบ มีความใฝ่เรียนรู้ ก้าวทันการเปลี่ยนแปลงของเทคโนโลยีด้านวิทยาการข้อมูลและการวิเคราะห์ พร้อมด้วยคุณธรรมและจริยธรรม เนื่องด้วยปัจจุบันระบบเศรษฐกิจและสังคมดิจิทัลทำให้เกิดการเปลี่ยนแปลงโครงสร้างของกิจกรรมทางเศรษฐกิจ ประกอบด้วย กระบวนการผลิต การค้า การบริการ การมีปฏิสัมพันธ์ระหว่างบุคคล ตลอดจนกระบวนการทางสังคมอื่น ๆ ดังนั้น องค์กรและหน่วยงานต่าง ๆ ทั้งภาครัฐและเอกชน ได้ตระหนักและเล็งเห็นถึงบทบาทความสำคัญ และความจำเป็นของข้อมูลและสารสนเทศ ที่ถูกนำมาใช้ในการวิเคราะห์และพยากรณ์ เพื่อให้ได้ผลลัพธ์ที่สามารถช่วยในการดำเนินงาน รวมทั้งการตัดสินใจและแก้ปัญหาได้อย่างถูกต้องรวดเร็ว สามารถขับเคลื่อนองค์กรและหน่วยงานไปสู่ความสำเร็จตามเป้าหมาย หลักสูตรได้ตระหนักถึงความสำคัญ และความจำเป็นที่จะต้องจัดเตรียมบุคลากรที่มีความรู้ทางด้านวิทยาการข้อมูลและการวิเคราะห์ เพื่อรองรับความต้องการของตลาดแรงงาน รวมทั้งรองรับการเปลี่ยนแปลงของเทคโนโลยีที่ได้รับการพัฒนาและเปลี่ยนแปลงอย่างรวดเร็ว</p>
